     /*****Fixed Menu******/
     var secondaryNav = $('.cd-secondary-nav'),
        secondaryNavTopPosition = secondaryNav.offset().top;
        navbar_height = document.querySelector('.navbar').offsetHeight;
        
       $(window).on('scroll', function(){
